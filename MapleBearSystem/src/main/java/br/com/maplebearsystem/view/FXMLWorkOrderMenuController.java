	private void autoReloadFXML() {

		try {

			FXMLWorkOrderMenuController controller;

			if (parentPane instanceof VBox) {
				controller = FXUISetup.getInstance()
						.loadFXMLIntoVbox((VBox) parentPane, MapleBearSystemDesktopClient.class,
								FXMLResourcePathsEnum.FXML_DWOR_WORKORDERMENU.getPath())
						.<FXMLWorkOrderMenuController>getController();
			} else {
				controller = FXUISetup.getInstance()
						.loadFXMLIntoPane(parentPane, MapleBearSystemDesktopClient.class,
								FXMLResourcePathsEnum.FXML_DWOR_WORKORDERMENU.getPath())
						.<FXMLWorkOrderMenuController>getController();
			}
			
			controller.setSourceFXMLController(sourceController);
			controller.setParentPane(parentPane);

		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

// ENDSECTION UISetup FXMLController Methods

