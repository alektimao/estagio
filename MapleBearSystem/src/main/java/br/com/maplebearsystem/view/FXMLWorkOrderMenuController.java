	@FXML
	public void actOpenNewBudgetOrder(ActionEvent event) {
		try {
			FXMLWorkOrderEditor controller = FXUISetup.getInstance()
					.loadFXMLIntoVbox((VBox) rootPane.getParent(), MapleBearSystemDesktopClient.class,
							FXMLResourcePathsEnum.FXML_DWOR_WORKORDEREDITOR.getPath())
					.<FXMLWorkOrderEditor>getController();

			controller.setSourceFXMLController(this);

		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

	@FXML
	public void actOpenNewWorkOrder(ActionEvent event) {
		try {
			FXMLWorkOrderEditor controller = FXUISetup.getInstance()
					.loadFXMLIntoVbox((VBox) rootPane.getParent(), MapleBearSystemDesktopClient.class,
							FXMLResourcePathsEnum.FXML_DWOR_WORKORDEREDITOR.getPath())
					.<FXMLWorkOrderEditor>getController();

			controller.setSourceFXMLController(this);
			controller.setHomeFXMLController(sourceController);

			((FXMLDworHomeController) sourceController).hideSidebar(true);

		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

